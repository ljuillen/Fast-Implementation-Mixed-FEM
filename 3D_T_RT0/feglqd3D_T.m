function [point2,weight2] = feglqd3D_T(ngl)
%
% function [xa,ya,za,wt] = feglqd3D_T(ngl)
% 
% Determine the integration points and weighting coefficients of 
% Gauss-Legendre quadrature for three-dim. integration on a tetrahedron.
% as in Hughes p. 174 (cases 1--3), for case > 3, see:
%  P Keast, Moderate degree tetrahedral quadrature formulas, CMAME 55: 339-348 (1986)
