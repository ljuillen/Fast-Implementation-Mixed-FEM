% and also 
% http://www.cfd-online.com/Wiki/Code:_Quadrature_on_Tetrahedra
% ----------------------------------------------------------------------
% by Bedrich Sousedik, May 2016.

% find corresponding integration points and weights
switch ngl
   case 1 % 1-point formula, degree of precision 1
      point2 = [1/4 1/4 1/4];
      weight2 = 1;
   case 2 % 4-point formula, degree of precision 2
      % points 0.58541020 and 0.13819660
      point2 = [ 0.5854101966249685 0.1381966011250105 0.1381966011250105;
